namespace MVCClient;

public class Program
{
    public static void Main(string[] args)
    {
        var builder = WebApplication.CreateBuilder(args);

        // Configure logging
        builder.Logging.ClearProviders();
        builder.Logging.AddConsole();
        builder.Logging.AddEventLog();

        // Add services to the container (from ConfigureServices)
        builder.Services.AddControllersWithViews();
        builder.Services.AddDbContext<OctobridgeContext>(options =>
            options.UseSqlServer(builder.Configuration.GetConnectionString("OctobridgeDatabase")));

        var app = builder.Build();

        // Configure the HTTP request pipeline (from Configure method)
        if (app.Environment.IsDevelopment())
        {
            app.UseDeveloperExceptionPage();
        }
        else
        {
            app.UseExceptionHandler("/Home/Error");
            // The default HSTS value is 30 days. You may want to change this for production scenarios, see https://aka.ms/aspnetcore-hsts.
            app.UseHsts();
        }

        app.UseHttpsRedirection();
        app.UseStaticFiles();

        app.UseRouting();

        app.UseAuthorization();

        app.MapControllerRoute(
            name: "default",
            pattern: "{controller=Home}/{action=Index}/{id?}");

        app.Run();
    }
}
